#include <iostream>
#include <cmath>

using namespace std;

int main() {

    cout << pow(2, 5) << endl; // will return 2 to the power of 5 -----> 32
    cout << sqrt(36) << endl; // will return square root of 36 -----> 6
    cout << round(4.6) << endl; // will return rounded number -----> 5
    cout << ceil(5.2) << endl; // will always return number rounded up ----> 6
    cout << floor(3.8) << endl; // will always return number rounded down ----> 3

    return 0;
}